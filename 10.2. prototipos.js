// Prototipos

/* Mecanismo por el cual todos los objetos o 
elementos de JavasScript pueden extender sus propiedades y métodos.
Nota:  Este concepto está muy ligado a los métodos que poseen
los objetos y las clases.*/

// Ejemplo:

// EL TIPO DE DATO ARRAY proviene del OBJETO GLOBAL LLAMADO ARRAY 
// Este objeto global le extiende mediante su PROTOTIPO
// propiedades y métodos al tipo de dato Array.
// Lo mismo ocurre con los objetos, números, string, e incluso
// con las clases que podemos crear. 

// El proceso en el que los objetos
// globales de JavaScript  le extienden métodos y propiedades a
// cualquier tipo de dato se denomina HERENCIA.

// Todos los objetos pueden heredar propiedades y métodos por medio
// de un prototipo. Gracias a estos prototipos podemos acceder al
// constructor de cualquer objeto para modificarlo